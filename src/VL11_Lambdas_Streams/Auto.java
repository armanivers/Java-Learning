package VL11_Lambdas_Streams;

public class Auto {
	private String name;
	private int geschwindigkeit;
	
	public Auto(String name, int gesch) {
		this.name = name;
		this.geschwindigkeit = gesch;
	}
	
	public String getName() {
		return this.name;
	}
	
	public int getGeschwindigkeit() {
		return this.geschwindigkeit;
	}
}
