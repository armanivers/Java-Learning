package Klausur06x10x2020.A2;

public class Apfel {
	private String farbe;
	private double gewicht;
	
	public Apfel(String farbe, int gewicht) {
		this.farbe = farbe;
		this.gewicht = gewicht;
	}
	
	public double getGewicht() {
		return this.gewicht;
	}
	
	public String getFarbe() {
		return this.farbe;
	}
}
